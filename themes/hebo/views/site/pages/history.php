<h1>History of RosaFlor</h1>


<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'><o:p>&nbsp;</o:p></b></p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'>ROSA FLOR ETERNAL GARDEN</b> has its humble beginning. Its name came
from two important persons, <b style='mso-bidi-font-weight:normal'>ROSA</b>
grandmother of Madam <span class=SpellE>Erlinda</span> <span class=SpellE><span
class=GramE>Baladad</span></span><span class=GramE>(</span>mother side) and a
mother of three sisters <b style='mso-bidi-font-weight:normal'>FLORENTINA</b>,
the eldest<b style='mso-bidi-font-weight:normal'>, JUANITA</b> , the second and
the youngest is<b style='mso-bidi-font-weight:normal'> CONCORDIA</b>(Mother of
Madam <span class=SpellE>Erlinda</span> <span class=SpellE>Baladad</span>)</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>FLORENTINA, the eldest is a widow
and childless. A very loving sister and aunt, she then concentrated her
properties to her 2 younger sisters. The ancestral house was awarded to Juanita,
and the lot where Rosa <span class=SpellE>Flor</span> Eternal Garden was
developed, she awarded to Concordia.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Real generous, FLORENTINA showed
her motherly love to her nephews and nieces, as great evidence, she distributed
to them her residential lots where their houses was constructed and built.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Many years may pass, from
generation to generation within their family, the love and honor they always
give and offer to “FLORENTINA<span class=GramE>”(</span><span class=SpellE>Flor</span>).
Their identity will forever remain in their hearts, “<b style='mso-bidi-font-weight:
normal'>ROSA</b>” (grandmother), “<b style='mso-bidi-font-weight:normal'>FLORENTINA”(<span
class=SpellE>Flor</span>)</b> their deepest respect and they salute to their
name after them <b style='mso-bidi-font-weight:normal'>ROSA FLOR Eternal Garden</b>.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>So its name resounds to everyone
today:</p>

